from solution import calc_magnitude, explode, split, add, needs_explode, needs_split


def test_needs():
    assert needs_explode([[[[[4, 3], 4], 4], [7, [[8, 4], 9]]], [1, 1]])
    assert needs_explode([[[[0, 7], 4], [7, [[8, 4], 9]]], [1, 1]])
    assert needs_split([[[[0, 7], 4], [15, [0, 13]]], [1, 1]])
    assert needs_split([[[[0, 7], 4], [[7, 8], [0, 13]]], [1, 1]])
    assert needs_explode([[[[0, 7], 4], [[7, 8], [0, [6, 7]]]], [1, 1]])


def test_magnitude():
    assert 143 == calc_magnitude([[1,2],[[3,4],5]])
    assert 1384 == calc_magnitude([[[[0,7],4],[[7,8],[6,0]]],[8,1]])
    assert 445 == calc_magnitude([[[[1,1],[2,2]],[3,3]],[4,4]])
    assert 791 == calc_magnitude([[[[3,0],[5,3]],[4,4]],[5,5]])
    assert 1137 == calc_magnitude([[[[5,0],[7,4]],[5,5]],[6,6]])
    assert 3488 == calc_magnitude([[[[8,7],[7,7]],[[8,6],[7,7]]],[[[0,7],[6,6]],[8,7]]])


def test_explode():
    assert [[[[0,9],2],3],4] == explode([[[[[9,8],1],2],3],4])
    assert [7,[6,[5,[7,0]]]] == explode([7,[6,[5,[4,[3,2]]]]])
    assert [[6,[5,[7,0]]],3] == explode([[6,[5,[4,[3,2]]]],1])
    assert [[3,[2,[8,0]]],[9,[5,[4,[3,2]]]]] == explode([[3,[2,[1,[7,3]]]],[6,[5,[4,[3,2]]]]])
    assert [[3,[2,[8,0]]],[9,[5,[7,0]]]] == explode([[3,[2,[8,0]]],[9,[5,[4,[3,2]]]]])


def test_add():
    assert [[[[4,0],[5,4]],[[7,7],[6,0]]],[[8,[7,7]],[[7,9],[5,0]]]] == add([[[0,[4,5]],[0,0]],[[[4,5],[2,6]],[9,5]]], [7,[[[3,7],[4,3]],[[6,3],[8,8]]]])
    assert add([[[[4,0],[5,4]],[[7,7],[6,0]]],[[8,[7,7]],[[7,9],[5,0]]]], [[2,[[0,8],[3,4]]],[[[6,7],1],[7,[1,6]]]]) == [[[[6,7],[6,7]],[[7,7],[0,7]]],[[[8,7],[7,7]],[[8,8],[8,0]]]]
    assert add([[[[6,7],[6,7]],[[7,7],[0,7]]],[[[8,7],[7,7]],[[8,8],[8,0]]]], [[[[2,4],7],[6,[0,5]]],[[[6,8],[2,8]],[[2,1],[4,5]]]]) == [[[[7,0],[7,7]],[[7,7],[7,8]]],[[[7,7],[8,8]],[[7,7],[8,7]]]]
    assert add([[[[7,0],[7,7]],[[7,7],[7,8]]],[[[7,7],[8,8]],[[7,7],[8,7]]]], [7,[5,[[3,8],[1,4]]]]) == [[[[7,7],[7,8]],[[9,5],[8,7]]],[[[6,8],[0,8]],[[9,9],[9,0]]]]
    assert add([[[[7,7],[7,8]],[[9,5],[8,7]]],[[[6,8],[0,8]],[[9,9],[9,0]]]], [[2,[2,2]],[8,[8,1]]]) == [[[[6,6],[6,6]],[[6,0],[6,7]]],[[[7,7],[8,9]],[8,[8,1]]]]
    assert add([[[[6,6],[6,6]],[[6,0],[6,7]]],[[[7,7],[8,9]],[8,[8,1]]]], [2,9]) == [[[[6,6],[7,7]],[[0,7],[7,7]]],[[[5,5],[5,6]],9]]
    assert add([[[[6,6],[7,7]],[[0,7],[7,7]]],[[[5,5],[5,6]],9]], [1,[[[9,3],9],[[9,0],[0,7]]]]) == [[[[7,8],[6,7]],[[6,8],[0,8]]],[[[7,7],[5,0]],[[5,5],[5,6]]]]
    assert add([[[[7,8],[6,7]],[[6,8],[0,8]]],[[[7,7],[5,0]],[[5,5],[5,6]]]], [[[5,[7,4]],7],1]) == [[[[7,7],[7,7]],[[8,7],[8,7]]],[[[7,0],[7,7]],9]]
    assert add([[[[7,7],[7,7]],[[8,7],[8,7]]],[[[7,0],[7,7]],9]], [[[[4,2],2],6],[8,7]]) == [[[[8,7],[7,7]],[[8,6],[7,7]]],[[[0,7],[6,6]],[8,7]]]


def test_data():
    test_data_ = [
        [[[0, [5, 8]], [[1, 7], [9, 6]]], [[4, [1, 2]], [[1, 4], 2]]],
        [[[5, [2, 8]], 4], [5, [[9, 9], 0]]],
        [6, [[[6, 2], [5, 6]], [[7, 6], [4, 7]]]],
        [[[6, [0, 7]], [0, 9]], [4, [9, [9, 0]]]],
        [[[7, [6, 4]], [3, [1, 3]]], [[[5, 5], 1], 9]],
        [[6, [[7, 3], [3, 2]]], [[[3, 8], [5, 7]], 4]],
        [[[[5, 4], [7, 7]], 8], [[8, 3], 8]],
        [[9, 3], [[9, 9], [6, [4, 9]]]],
        [[2, [[7, 7], 7]], [[5, 8], [[9, 3], [0, 2]]]],
        [[[[5, 2], 5], [8, [3, 7]]], [[5, [7, 5]], [4, 4]]],
    ]
    sum_ = test_data_[0]
    for op in test_data_[1:]:
        sum_ = add(sum_, op)
    assert [[[[6,6],[7,6]],[[7,7],[7,0]]],[[[7,7],[7,7]],[[7,8],[9,9]]]] == sum_
    assert 4140 == calc_magnitude(sum_)
